<template>
  <div class="cartcontrol">
     <div class="cart-desrease" v-show="food.count>0" @click="desreaseCart" transition="move">
     	<span class="inner icon-remove_circle_outline"></span>
     </div> 
     <div class="cart-count" v-show="food.count>0">{{food.count}}</div> 
     <div class="cart-add icon-add_circle" @click="addCart"></div> 
    	
  </div>
</template>

<script>

import Vue from 'vue';
export default {
	props:{
		food:{
			type:Object
		}
	},
	methods:{
		addCart(event){
			if(!event._constructed){
				return ;
			}
			if(!this.food.count){
				Vue.set(this.food,'count',1);
			}else{
				this.food.count++;
			}
			this.$dispatch('cart.add',event.target);
		},
		desreaseCart(){
			if(!event._constructed){
				return ;
			}
			if(this.food.count){
				this.food.count--;
			}
		}
	}
	
	
}
</script>


